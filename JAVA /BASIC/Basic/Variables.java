package Basic;
public class Variables {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println("Sum of a and b is: " + c);

        String name = "John Doe";
        System.out.println("Name: " + name);

        double pi = 3.14159;
        System.out.println("Value of pi: " + pi);
    }
}
